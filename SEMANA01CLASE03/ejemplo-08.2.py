
"agregar un item al final de nuestra lista usando la iteracion for"

"10 primeros numeros al cuadrado"

lista = []
for item in range(10):
 lista.append(item**2)
print("el valor de mi lista es :\n ", lista)

"15 primeros numeros impares"

lista1 = []
