"uso del flujo condicional if"

var1 = int(input("ingrese el primer valor: "))
var2 = int(input("ingrese el segundo valor: "))
if var1 > var2: # si es verdadero ejecuta su logica interna del flujo condicional
    print("el valor de var1 es mayor que la segunda variable")
    print("es verdadero")
else:
    print("el valor de var1 no es mayor que la segunda variable")
    print("la sentencia es falsa")
